package fr.yr.application.calculatrice;

public class Calcul {

    public Calcul() {

    }

    public double somme(float val1,float val2) {
        return val1+val2;
    }

    public double soustraction(float val1,float val2) {
        return val1-val2;
    }

    public double produit(float val1,float val2) {
        return val1*val2;
    }

    public double div(float val1,float val2) {
        return val1/val2;
    }
}
